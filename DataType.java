package cisco.java.programs;

public class DataType {

	public static void main(String[] args) {
		
		char gender = 'M';
		System.out.println("The value of gender is "+ gender);
		int age = 34;
		System.out.println("The value of age is "+ age);
		long distance = 1577879l;
		System.out.println("The value of age is "+ distance);
		float Pi = 3.147f;
		System.out.println("The value of age is "+ Pi);
		byte b = 123;
		System.out.println("The value of age is "+ b);
		boolean y= true;
		System.out.println("The value of age is "+ y);
	}
}
